create or replace function public.save_bet_board_state(
  p_board_id text,
  p_state jsonb
)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  v_match record;
  v_participant record;
  v_round record;
  v_score record;
  v_match_id text;
  v_round_no integer;
begin
  insert into public.bet_boards (id, active_match_id, app_version, updated_at)
  values (
    p_board_id,
    p_state->>'activeMatchId',
    coalesce((p_state->>'version')::integer, 11),
    now()
  )
  on conflict (id) do update
  set
    active_match_id = excluded.active_match_id,
    app_version = excluded.app_version,
    updated_at = now();

  delete from public.bet_matches
  where board_id = p_board_id;

  for v_match in
    select value as data, ordinality::integer as sort_order
    from jsonb_array_elements(coalesce(p_state->'matches', '[]'::jsonb)) with ordinality
  loop
    v_match_id := v_match.data->>'id';

    insert into public.bet_matches (
      id,
      board_id,
      title,
      match_date,
      dinner_price,
      settlement_mode,
      fund_rule,
      my_participant_id,
      match_order,
      updated_at
    )
    values (
      v_match_id,
      p_board_id,
      coalesce(v_match.data->>'title', '오늘 내기'),
      coalesce(v_match.data->>'date', ''),
      coalesce((v_match.data->>'dinnerPrice')::integer, 0),
      coalesce(v_match.data->>'settlementMode', 'share-ratio'),
      coalesce(v_match.data->'fundRule', '{"roundAmount":10000,"rankAllocations":[2000,3000,5000]}'::jsonb),
      v_match.data->>'myParticipantId',
      v_match.sort_order,
      coalesce((v_match.data->>'updatedAt')::timestamptz, now())
    );

    for v_participant in
      select value as data, ordinality::integer as sort_order
      from jsonb_array_elements(coalesce(v_match.data->'participants', '[]'::jsonb)) with ordinality
    loop
      insert into public.bet_participants (
        match_id,
        id,
        name,
        initial_handicap,
        participant_order
      )
      values (
        v_match_id,
        v_participant.data->>'id',
        coalesce(v_participant.data->>'name', '참가자'),
        coalesce((v_participant.data->>'initialHandicap')::integer, 0),
        v_participant.sort_order
      );
    end loop;

    for v_round in
      select value as data, ordinality::integer as sort_order
      from jsonb_array_elements(coalesce(v_match.data->'history', '[]'::jsonb)) with ordinality
    loop
      v_round_no := coalesce((v_round.data->>'round')::integer, v_round.sort_order);

      insert into public.bet_rounds (
        match_id,
        round_no,
        rule,
        is_draw,
        winner_id,
        loser_id,
        course_name,
        holes_played,
        completion_status,
        partial_round_policy,
        fund_rule,
        round_order
      )
      values (
        v_match_id,
        v_round_no,
        coalesce(v_round.data->>'rule', 'field-average'),
        coalesce((v_round.data->>'isDraw')::boolean, false),
        v_round.data->>'winnerId',
        v_round.data->>'loserId',
        nullif(v_round.data->>'courseName', ''),
        coalesce((v_round.data->>'holesPlayed')::integer, 18),
        coalesce(v_round.data->>'completionStatus', 'completed'),
        v_round.data->>'partialRoundPolicy',
        v_round.data->'fundRule',
        v_round.sort_order
      );

      for v_score in
        select value as data, ordinality::integer as sort_order
        from jsonb_array_elements(coalesce(v_round.data->'scores', '[]'::jsonb)) with ordinality
      loop
        insert into public.bet_round_scores (
          match_id,
          round_no,
          participant_id,
          strokes,
          score_order
        )
        values (
          v_match_id,
          v_round_no,
          v_score.data->>'participantId',
          coalesce((v_score.data->>'strokes')::integer, 0),
          v_score.sort_order
        );
      end loop;
    end loop;
  end loop;

  insert into public.bet_board_state (id, state, updated_at)
  values (p_board_id, p_state, now())
  on conflict (id) do update
  set
    state = excluded.state,
    updated_at = now();
end;
$$;

create or replace function public.get_bet_board_state(p_board_id text)
returns jsonb
language sql
stable
security definer
set search_path = public
as $$
  select case
    when b.id is null then null
    else jsonb_build_object(
      'version', b.app_version,
      'activeMatchId', b.active_match_id,
      'matches', coalesce(
        (
          select jsonb_agg(
            jsonb_strip_nulls(
              jsonb_build_object(
                'id', m.id,
                'title', m.title,
                'date', m.match_date,
                'dinnerPrice', m.dinner_price,
                'settlementMode', m.settlement_mode,
                'fundRule', m.fund_rule,
                'myParticipantId', m.my_participant_id,
                'participants', coalesce(
                  (
                    select jsonb_agg(
                      jsonb_build_object(
                        'id', p.id,
                        'name', p.name,
                        'initialHandicap', p.initial_handicap
                      )
                      order by p.participant_order
                    )
                    from public.bet_participants p
                    where p.match_id = m.id
                  ),
                  '[]'::jsonb
                ),
                'history', coalesce(
                  (
                    select jsonb_agg(
                      jsonb_strip_nulls(
                        jsonb_build_object(
                          'round', r.round_no,
                          'scores', coalesce(
                            (
                              select jsonb_agg(
                                jsonb_build_object(
                                  'participantId', s.participant_id,
                                  'strokes', s.strokes
                                )
                                order by s.score_order
                              )
                              from public.bet_round_scores s
                              where s.match_id = r.match_id
                                and s.round_no = r.round_no
                            ),
                            '[]'::jsonb
                          ),
                          'isDraw', r.is_draw,
                          'loserId', r.loser_id,
                          'rule', r.rule,
                          'winnerId', r.winner_id,
                          'courseName', r.course_name,
                          'holesPlayed', r.holes_played,
                          'completionStatus', r.completion_status,
                          'partialRoundPolicy', r.partial_round_policy,
                          'fundRule', r.fund_rule
                        )
                      )
                      order by r.round_order
                    )
                    from public.bet_rounds r
                    where r.match_id = m.id
                  ),
                  '[]'::jsonb
                ),
                'updatedAt', to_char(m.updated_at at time zone 'utc', 'YYYY-MM-DD"T"HH24:MI:SS.MS"Z"')
              )
            )
            order by m.match_order
          )
          from public.bet_matches m
          where m.board_id = b.id
        ),
        '[]'::jsonb
      )
    )
  end
  from public.bet_boards b
  where b.id = p_board_id;
$$;

create or replace function public.migrate_bet_board_state_to_relational()
returns integer
language plpgsql
security definer
set search_path = public
as $$
declare
  v_record record;
  v_count integer := 0;
begin
  for v_record in
    select id, state
    from public.bet_board_state
  loop
    perform public.save_bet_board_state(v_record.id, v_record.state);
    v_count := v_count + 1;
  end loop;

  return v_count;
end;
$$;

revoke all on function public.get_bet_board_state(text) from public;
