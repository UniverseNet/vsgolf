create table if not exists public.bet_board_state (
  id text primary key,
  state jsonb not null,
  updated_at timestamptz not null default now()
);

create table if not exists public.bet_boards (
  id text primary key,
  active_match_id text,
  app_version integer not null default 11,
  updated_at timestamptz not null default now()
);

create table if not exists public.bet_matches (
  id text primary key,
  board_id text not null references public.bet_boards(id) on delete cascade,
  title text not null,
  match_date text not null,
  dinner_price integer not null default 0,
  settlement_mode text not null default 'share-ratio',
  fund_rule jsonb not null default '{"roundAmount":10000,"rankAllocations":[2000,3000,5000]}'::jsonb,
  my_participant_id text,
  match_order integer not null default 0,
  updated_at timestamptz not null default now()
);

create table if not exists public.bet_participants (
  match_id text not null references public.bet_matches(id) on delete cascade,
  id text not null,
  name text not null,
  initial_handicap integer not null default 0,
  participant_order integer not null default 0,
  primary key (match_id, id)
);

create table if not exists public.bet_rounds (
  id bigint generated by default as identity primary key,
  match_id text not null references public.bet_matches(id) on delete cascade,
  round_no integer not null,
  rule text not null default 'field-average',
  is_draw boolean not null default false,
  winner_id text,
  loser_id text,
  course_name text,
  holes_played integer not null default 18,
  completion_status text not null default 'completed',
  partial_round_policy text,
  fund_rule jsonb,
  round_order integer not null default 0,
  unique (match_id, round_no)
);

create table if not exists public.bet_round_scores (
  match_id text not null,
  round_no integer not null,
  participant_id text not null,
  strokes integer not null,
  score_order integer not null default 0,
  primary key (match_id, round_no, participant_id),
  foreign key (match_id, round_no) references public.bet_rounds(match_id, round_no) on delete cascade,
  foreign key (match_id, participant_id) references public.bet_participants(match_id, id) on delete cascade
);

create index if not exists bet_matches_board_order_idx
on public.bet_matches(board_id, match_order);

create index if not exists bet_participants_match_order_idx
on public.bet_participants(match_id, participant_order);

create index if not exists bet_rounds_match_order_idx
on public.bet_rounds(match_id, round_order);

create index if not exists bet_round_scores_round_order_idx
on public.bet_round_scores(match_id, round_no, score_order);

